package dto;

/**
 *
 * @author zoro
 */
public class Especialidad {
    private int idEspecialidad;
    private String nombreEspecialidad;
    private String decripcionEspecialidad;

    /**
     * @return the idEspecialidad
     */
    public int getIdEspecialidad() {
        return idEspecialidad;
    }

    /**
     * @param idEspecialidad the idEspecialidad to set
     */
    public void setIdEspecialidad(int idEspecialidad) {
        this.idEspecialidad = idEspecialidad;
    }

    /**
     * @return the nombreEspecialidad
     */
    public String getNombreEspecialidad() {
        return nombreEspecialidad;
    }

    /**
     * @param nombreEspecialidad the nombreEspecialidad to set
     */
    public void setNombreEspecialidad(String nombreEspecialidad) {
        this.nombreEspecialidad = nombreEspecialidad;
    }

    /**
     * @return the decripcionEspecialidad
     */
    public String getDecripcionEspecialidad() {
        return decripcionEspecialidad;
    }

    /**
     * @param decripcionEspecialidad the decripcionEspecialidad to set
     */
    public void setDecripcionEspecialidad(String decripcionEspecialidad) {
        this.decripcionEspecialidad = decripcionEspecialidad;
    }
    
    
}
